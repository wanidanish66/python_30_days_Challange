s1 = {1, 2, 3, 4, 5}
s2 = {4, 5, 6, 7, 8}

print("Union of s1 and s2:", s1.union(s2)) # this prints the union of s1 and s2 which is a set of all unique elements form both sets
print("Intersection of s1 and s2:", s1.intersection(s2)) # this prints the intersection of s1 and s2 which is a set of all elements that are common in both sets
