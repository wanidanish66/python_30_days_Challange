name = "micro"
print(name)

length = len(name)  # returns the length of the string
print(length)

string = name.endswith("o")  # checks if the string ends with "o"
print(string)

string = name.startswith("m")  # checks if the string starts with "m"
print(string)

string = name.count("c")  # counts the number of occurrences of "c" in the string
print(string)

string = name.find("r")  # finds the index of the first occurrence of "r" in the string
print(string)

string = name.capitalize()  # capitalizes the first letter of the string
print(string)

string = name.upper()  # converts the string to uppercase
print(string)
