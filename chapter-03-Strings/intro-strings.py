name = "micro"
print(name)

slice = name[0:2]   # slicing the string from index 0 to index 2 (exclusive)
print(slice)

sl = name[-2:-1]  # slicing the string from index -2 to index -1 (exclusive)
print(sl)

word = "python"
sl = word[0:5:2]  # slicing the string from index 0 to index 5 (exclusive) with a step of 2
print(sl)