l1 = [1, 8, 7, 2, 21, 15]

l1.sort() # sorts the list in ascending order
print(l1)

l1.reverse() # reverses the order of the list
print(l1)

l1.append(10) # adds an element to the end of the list
print(l1)

l1.insert(2, 5) # this will insert the value 5 at index 2
print(l1)

l1.pop(2) # removes the element at index 2 from the list
print(l1)

l1.remove(10) # removes the first occurrence of the value 10 from the list
print(l1)

