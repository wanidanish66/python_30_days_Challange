# tuples in python are immutable , they cannot be changed after they are created. they are defined using parentheses ()  

a = () # this is tuple with no elements

b = (3,) # this is a tuple with one element, we need to add a comma after the element to indicate that it is a tuple

c = (1, 2, 3, 4, 5) # this is a tuple with multiple elements

print(a)
print(b)
print(c)

# once defined a tuple element cannot be changeed, we cannot add or remove elements from a tuple, we cannot change the value of an element in a tuple